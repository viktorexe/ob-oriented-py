name = 'vansh    sharma UPPER             '

# Makes whole string uppercase
print('Uppercase string:',name.upper())

# Makes whole string lowercase
print('Lowercase string:',name.lower())

# Makes first letter of every word uppercase
print('First letter of all words uppercase:',name.upper())

# Makes first letter only capital, of the entire string
print('First letter only uppercase:',name.capitalize())

# Makes uppercase <-> lowercase
print("Swap function:",name.swapcase())

# Removes extra space
print("Extra space removed:",name.strip())

# Removes space from right
print("Shifts one tab to right:",name.rstrip())

# Removes space from left
print("Shifts one tab to left:",name.lstrip())