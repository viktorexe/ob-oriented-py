# List operations (Adding)

main_list = [1, 2, 3, 4, 5, 6]

# Append: Adds element to end of the lsit
print("Main list is:", main_list)
main_list.append(1)            
print("Modified append list is:", main_list)

# Insert: Adds at specific index
print("Main list is:", main_list)
main_list.insert(1, 10)
print("Modified insert list is:", main_list)

# Extend: Add multiple items but at end
print("Main list is:", main_list)
main_list.extend([99, 88])
print("Modified extend list is:", main_list)

