# List operations (Remove)

main_list = [1, 2, 3, 4, 5, 6]

# Remove: Enter the element to remove
print("Main list is:", main_list)
main_list.remove(3)
print("Modified remove list is:", main_list)

# Pop: Remove by index
print("Main list is:", main_list)
main_list.pop(0)
print("Modified pop list is:", main_list)